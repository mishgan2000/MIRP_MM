#ifndef DD_MIRP_H_
#define DD_MIRP_H_


#define EEPROM_ADR					0x50
#define TOR_SECTOR1					0x40		// first tor bin
#define TOR_SECTOR1_START_CNT		0x58		// first tor bin from start cnt and then SN and ToolType
#define TOR_SECTOR2					0x80		// second tor bin
#define TOR_SECTOR2_START_CNT		0x98		// second tor bin from start cnt
#define TOR_SECTOR_LENGTH			28			// 24 bytes of operating time + StartCnt + ToolSN
#define EE_CALIB_OFFSET				0x200		// the beginning of the calibration file
#endif /* DD_MIRP_H_ */
